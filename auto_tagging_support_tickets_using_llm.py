"""Zero-Shot Classification Function"""

def zero_shot_classify(ticket_text, top_k=3):
    """Zero-Shot: No examples in prompt. LLM classifies purely from instruction."""
    prompt = f"""You are a customer support ticket classifier.

Classify the following support ticket into ONE of these exact categories:
- Billing and Payments
- Customer Service
- IT Support
- Product Support
- Technical Support

Return a JSON object with this exact format:
{{
  "top1": {{"tag": "category_name", "confidence": 0.95}},
  "top2": {{"tag": "category_name", "confidence": 0.03}},
  "top3": {{"tag": "category_name", "confidence": 0.02}}
}}

Rules:
- tag values must exactly match one of the 5 category names listed above
- top1 is your best prediction
- top2 and top3 must be different from top1 and each other
- confidences must sum to 1.0
- Return ONLY the JSON, nothing else

Support Ticket: "{ticket_text[:300]}"
"""
    try:
        response = client.chat.completions.create(
            model=GROQ_MODEL,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.1,
            max_tokens=200
        )
        raw = response.choices[0].message.content.strip()
        match = re.search(r'\{.*\}', raw, re.DOTALL)
        if match:
            result = json.loads(match.group())
            top1 = result['top1']['tag']
            top2 = result['top2']['tag']
            top3 = result['top3']['tag']
            s1 = result['top1']['confidence']
            s2 = result['top2']['confidence']
            s3 = result['top3']['confidence']
            return [top1, top2, top3], [s1, s2, s3]
    except Exception as e:
        print(f"  Warning: {e}")
    return [TAGS[0], TAGS[1], TAGS[2]], [0.6, 0.3, 0.1]

