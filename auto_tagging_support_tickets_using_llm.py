"""Fine-Tune DistilBERT"""

from transformers import (
    DistilBertTokenizer,
    DistilBertForSequenceClassification,
    Trainer, TrainingArguments,
    DataCollatorWithPadding
)
from datasets import Dataset

# Label encoding
label2id = {tag: i for i, tag in enumerate(TAGS)}
id2label = {i: tag for i, tag in enumerate(TAGS)}

# Encode labels
df['label'] = df['true_label'].map(label2id)

# Train/test split
train_df, test_df = train_test_split(
    df[['ticket_text', 'label']],
    test_size=0.2,
    random_state=42,
    stratify=df['label']
)

train_df = train_df.reset_index(drop=True)
test_df  = test_df.reset_index(drop=True)

print(f"Train size : {len(train_df)}")
print(f"Test size  : {len(test_df)}")

# Tokenize
ft_tokenizer = DistilBertTokenizer.from_pretrained("distilbert-base-uncased")

def tokenize(examples):
    return ft_tokenizer(
        examples["ticket_text"],
        truncation=True,
        padding=False,
        max_length=128
    )

train_dataset = Dataset.from_pandas(train_df)
test_dataset  = Dataset.from_pandas(test_df)

train_tokenized = train_dataset.map(tokenize, batched=True, remove_columns=["ticket_text"])
test_tokenized  = test_dataset.map(tokenize, batched=True, remove_columns=["ticket_text"])

print("✅ Tokenization complete")
print(f"Columns: {train_tokenized.column_names}")

"""Train DistilBERT"""

ft_model = DistilBertForSequenceClassification.from_pretrained(
    "distilbert-base-uncased",
    num_labels=len(TAGS),
    id2label=id2label,
    label2id=label2id
)

data_collator = DataCollatorWithPadding(tokenizer=ft_tokenizer)

def compute_metrics(eval_pred):
    logits, labels = eval_pred
    preds = np.argmax(logits, axis=-1)
    return {
        "accuracy": accuracy_score(labels, preds),
        "f1"      : f1_score(labels, preds, average='weighted')
    }

training_args = TrainingArguments(
    output_dir               = "./distilbert-tickets",
    num_train_epochs         = 10,
    per_device_train_batch_size = 8,
    per_device_eval_batch_size  = 8,
    warmup_steps             = 10,
    weight_decay             = 0.01,
    learning_rate            = 3e-5,
    eval_strategy            = "epoch",
    save_strategy            = "epoch",
    load_best_model_at_end   = True,
    metric_for_best_model    = "f1",
    logging_steps            = 5,
    report_to                = "none",
    fp16                     = torch.cuda.is_available()
)

trainer = Trainer(
    model            = ft_model,
    args             = training_args,
    train_dataset    = train_tokenized,
    eval_dataset     = test_tokenized,
    processing_class = ft_tokenizer,
    data_collator    = data_collator,
    compute_metrics  = compute_metrics
)

print("Starting DistilBERT fine-tuning...")
print("=" * 50)
trainer.train()
print("\n✅ Fine-tuning complete!")

"""Evaluate Fine-Tuned Model"""

